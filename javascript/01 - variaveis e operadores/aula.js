// Let --> Valor pode ser alterado
// Const --> Valor não pode ser alterado

// Operadores aritiméticos
let numero = 10;
let numero2 = 20;
// Soma
console.log(numero + numero2);

// Subtração
console.log(numero - numero2);

// Multiplicação
console.log(numero * numero2);

// Divisão
console.log(numero / numero2);
